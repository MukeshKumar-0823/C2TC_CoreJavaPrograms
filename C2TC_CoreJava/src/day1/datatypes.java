package day1;

public class datatypes {
	public static void main(String[] args) {
		int a=8;
		System.out.println(a);
		
		char c='A';
		System.out.println(c);
		
		String name="Mukesh";
		System.out.println(name);
		
		double d=a*0.22;
		System.out.println(d);
		
		Long phonenumber=1234567898L;
		System.out.println(phonenumber);
		
		
	}
}
