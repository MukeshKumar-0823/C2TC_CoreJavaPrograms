package com.MukeshKumarV.assignment.employees;

public class Employee {
	private int name;
	private int empid;
	private int salary;
	public int getName() {
		return name;
	}
	public void setName(int name) {
		this.name = name;
	}
	public int getEmpid() {
		return empid;
	}
	public void setEmpid(int empid) {
		this.empid = empid;
	}
	public int  getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	

}
